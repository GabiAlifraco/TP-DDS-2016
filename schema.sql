
    create table CGP (
        nombre varchar(255),
        poiID bigint not null,
        primary key (poiID)
    )

    create table Disponibilidad (
        horarioInicial varbinary(255) not null,
        horarioFinal varbinary(255),
        banco varchar(255) generated by default as identity (start with 1),
        Coordenada X varchar(255),
        Coordenada Y varchar(255),
        primary key (banco)
    )

    create table Domicilio (
        domicilioID bigint generated by default as identity (start with 1),
        alturaCalles integer not null,
        alturaPrincipal integer not null,
        callePrincipal varchar(255),
        codigoPostal integer not null,
        departamento integer not null,
        entreUnaCalle varchar(255),
        hastaOtraCalle varchar(255),
        piso integer not null,
        unidad integer not null,
        primary key (domicilioID)
    )

    create table POIS (
        poiID bigint generated by default as identity (start with 1),
        nombre varchar(255),
        ubicacion_ubicacionID bigint,
        primary key (poiID)
    )

    create table Region (
        RegionID bigint generated by default as identity (start with 1),
        barrio varchar(255),
        localidad varchar(255),
        pais varchar(255),
        provincia varchar(255),
        primary key (RegionID)
    )

    create table ServicioCGP (
        nombre varchar(255) generated by default as identity (start with 1),
        primary key (nombre)
    )

    create table Ubicacion (
        ubicacionID bigint generated by default as identity (start with 1),
        coordenadas varchar(255),
        domicilio_domicilioID bigint,
        region_RegionID bigint,
        primary key (ubicacionID)
    )

