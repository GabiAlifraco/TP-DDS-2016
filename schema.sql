
    create table Disponibilidad (
        idDisponibilidad bigint generated by default as identity (start with 1),
        horarioFinal varbinary(255),
        horarioInicial varbinary(255),
        id varbinary(255),
        poiID bigint,
        primary key (idDisponibilidad)
    )

    create table Disponibilidad_dias (
        Disponibilidad_idDisponibilidad bigint not null,
