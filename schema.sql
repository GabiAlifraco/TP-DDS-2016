    )

    create table Region (
        RegionID bigint generated by default as identity (start with 1),
        barrio varchar(255),
        localidad varchar(255),
        pais varchar(255),
        provincia varchar(255),
        primary key (RegionID)
    )

    create table ResultadosBusqueda (
        idResultado bigint generated by default as identity (start with 1),
        cantidadDeResultados integer not null,
        FechaBusqueda varbinary(255),
